package pl.wsei.mobilne.myapplication.space3d;

public class WallCoordinates {

    private float X;
    private float Z;

    public float getX() {
        return X;
    }

    public float getZ() {
        return Z;
    }

    public WallCoordinates(float x, float z) {
        X = x;
        Z = z;
    }
}